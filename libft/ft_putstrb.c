#include "libft.h"

void	ft_putstrb(char const *s, int *bytes)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
			*bytes = *bytes + 1;
		}
	}
}
